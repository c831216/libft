#include "libft.h"

int	ft_tolower(int c)
{
	unsigned int a = (unsigned int)c;

	if(c >= 'A' && c <= 'Z')
	{
		int dif = 'A' - 'a';
		a = a - dif;
	}
	return (a);
}
