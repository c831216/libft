#include "libft.h"

int	ft_toupper(int c)
{
	unsigned int nc;

	nc = (unsigned int)c;
	int dif = 'A' - 'a';

	if (c >= 'a' && c <= 'z')
	{
		nc = nc + dif;
	}
	return (nc);
}
